let readlineSync = require("readline-sync");
let genGrid = require("./grid.js");

let grid1 = readlineSync.question('How wide do you want you grid to be?  Please input a number: ');
let grid2 = readlineSync.question('How tall do you want your grid to be? Please input a number: ')

let grid = genGrid(grid1, grid2);

//prompts user to enter info (x coord, y coord, and character input)
//generate a grid that is 5 x 5 squares
//alter the original grid to display the chosen character at the chosen coordinates



while(true) {
  let yCoord = readlineSync.question(`Please choose a Y Coordinate between 0 and ${grid1 - 1}: `);
  let xCoord = readlineSync.question(`Please choose an X Coordinate between 0 and ${grid2 - 1}: `);
  let newLet = readlineSync.question('Please input a new character for the grid: ')
  grid[yCoord][xCoord] = newLet;
  console.log(grid);
  let keepPlaying = readlineSync.keyInYN("If you want to keep playing, type 'Y', if not type 'N'" );
  if(!keepPlaying) break;
}
